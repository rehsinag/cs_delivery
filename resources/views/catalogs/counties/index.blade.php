@section('scripts')
    <script>
        var Counties = {
            opts: {
                'actual': 1
            },

            list: function () {
                $.ajax({
                    url: '{{ route2('catalogs.counties.list') }}',
                    data: Counties.opts,
                    beforeSend: function(){csDeliveryPreloader('show')},
                    complete: function(){csDeliveryPreloader('hide')},
                    success: function(data){
                        $('.counties').html(data)
                    },
                    error: function(){
                        csDeliveryNotify(
                            'Возникла ошибка на сервере.. Пожалуйста, попробуйте позднее',
                            'danger'
                        );
                    }
                })
            },

            edit: function(countyId){
                if(typeof countyId=='undefined')
                    countyId=''
                $.ajax({
                    url: "{{ route2('catalogs.counties.edit') }}",
                    type: 'get',
                    data: 'countyId='+countyId,
                    beforeSend: function(){csDeliveryPreloader('show')},
                    complete: function(){csDeliveryPreloader('hide')},
                    success: function(resp){
                        $('#countyModal .modal-body').html(resp)
                        $("#countyModal").modal()
                    },
                    error: function(){
                        csDeliveryNotify(
                            'Возникла ошибка на сервере.. Пожалуйста, попробуйте позднее',
                            'danger'
                        );
                    }
                });
            },

            submit: function () {
                $.ajax({
                    url: "{{ route2('catalogs.counties.submit') }}",
                    type: 'post',
                    data: $('#countyModalForm').serialize(),
                    beforeSend: function(){csDeliveryPreloader('show')},
                    complete: function(){csDeliveryPreloader('hide')},
                    success: function(resp){
                        if(resp.success) {
                            $("#countyModal").modal('hide');
                            Counties.list();
                            csDeliveryNotify(resp.message, 'success');
                        } else {
                            $.notify({
                                // options
                                message: resp.message
                            },{
                                // settings
                                type: 'danger',
                                z_index: 2000,
                            });
                        }
                    },
                    error: function(){
                        csDeliveryNotify(
                            'Возникла ошибка на сервере.. Пожалуйста, попробуйте позднее',
                            'danger'
                        );
                    }
                });
            },

            delete: function (countyId) {
                if(!(confirm('Удалить отмеченную запись?'))) {
                    return false;
                }
                $.ajaxSetup({
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    }
                });
                $.ajax({
                    url: "{{ route2('catalogs.counties.delete') }}",
                    type: 'post',
                    data: 'countyId='+countyId,
                    beforeSend: function(){csDeliveryPreloader('show')},
                    complete: function(){csDeliveryPreloader('hide')},
                    success: function(resp){
                        if(resp.success) {
                            Counties.list();
                            csDeliveryNotify(resp.message, 'success');
                        } else {
                            csDeliveryNotify(resp.message, 'danger');
                        }
                    },
                    error: function(){
                        csDeliveryNotify(
                            'Возникла ошибка на сервере.. Пожалуйста, попробуйте позднее',
                            'danger'
                        );
                    }
                });
            },
        }
        $(document).ready(function () {
            Counties.list();
        })
    </script>
@endsection